        document.addEventListener('DOMContentLoaded', function () {
            const track = document.querySelector('.slider-track');
            const cards = document.querySelectorAll('.course-card');
            const prevBtn = document.querySelector('.slider-nav-prev');
            const nextBtn = document.querySelector('.slider-nav-next');
            const dots = document.querySelectorAll('.slider-dot');
            const container = document.querySelector('.slider-container');

            let currentIndex = 0;
            const totalCards = cards.length;

            // Get card width including gap
            function getCardWidth() {
                const card = cards[0];
                const style = window.getComputedStyle(card);
                const cardWidth = card.offsetWidth;
                const marginRight = parseInt(window.getComputedStyle(track).gap) || 30;
                return cardWidth + marginRight;
            }

            // Calculate how many cards are visible
            function getVisibleCards() {
                const containerWidth = container.offsetWidth;
                const cardWidth = getCardWidth();
                return Math.max(1, Math.floor(containerWidth / cardWidth));
            }

            // Calculate the maximum index
            function getMaxIndex() {
                const visibleCards = getVisibleCards();
                return Math.max(0, totalCards - visibleCards);
            }

            function updateSlider() {
                const cardWidth = getCardWidth();
                const containerWidth = container.offsetWidth;
                const visibleCards = getVisibleCards();

                // For mobile, scroll one card at a time and center it
                if (window.innerWidth <= 768) {
                    // Center the card in the viewport
                    const offset = -currentIndex * cardWidth + (containerWidth - cardWidth + 30) / 2;
                    track.style.transform = `translateX(${offset}px)`;
                } else {
                    // For desktop, normal behavior
                    const offset = -currentIndex * cardWidth;
                    track.style.transform = `translateX(${offset}px)`;
                }

                // Update dots
                dots.forEach((dot, index) => {
                    dot.classList.toggle('active', index === currentIndex);
                });

                // Update button states
                const maxIndex = getMaxIndex();
                prevBtn.disabled = currentIndex === 0;
                nextBtn.disabled = currentIndex >= maxIndex;
            }

            // Previous button
            prevBtn.addEventListener('click', () => {
                if (currentIndex > 0) {
                    currentIndex--;
                    updateSlider();
                }
            });

            // Next button
            nextBtn.addEventListener('click', () => {
                const maxIndex = getMaxIndex();
                if (currentIndex < maxIndex) {
                    currentIndex++;
                    updateSlider();
                }
            });

            // Dots navigation
            dots.forEach((dot, index) => {
                dot.addEventListener('click', () => {
                    const maxIndex = getMaxIndex();
                    currentIndex = Math.min(index, maxIndex);
                    updateSlider();
                });
            });

            // Keyboard navigation
            document.addEventListener('keydown', (e) => {
                if (e.key === 'ArrowLeft') {
                    prevBtn.click();
                } else if (e.key === 'ArrowRight') {
                    nextBtn.click();
                }
            });

            // Touch swipe support
            let touchStartX = 0;
            let touchEndX = 0;

            container.addEventListener('touchstart', (e) => {
                touchStartX = e.changedTouches[0].screenX;
            });

            container.addEventListener('touchend', (e) => {
                touchEndX = e.changedTouches[0].screenX;
                handleSwipe();
            });

            function handleSwipe() {
                if (touchEndX < touchStartX - 50) {
                    // Swipe left - next
                    nextBtn.click();
                }
                if (touchEndX > touchStartX + 50) {
                    // Swipe right - previous
                    prevBtn.click();
                }
            }

            // Update on window resize
            let resizeTimer;
            window.addEventListener('resize', () => {
                clearTimeout(resizeTimer);
                resizeTimer = setTimeout(() => {
                    // Reset to first slide on orientation change
                    if (window.innerWidth <= 768) {
                        currentIndex = Math.min(currentIndex, getMaxIndex());
                    }
                    updateSlider();
                }, 250);
            });

            // Initial update
            updateSlider();
        });


