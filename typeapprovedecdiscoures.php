    <script>
        // Course System - COMPLETELY FIXED VERSION
        (function () {
            'use strict';

            // Wait for DOM to be fully loaded
            if (document.readyState === 'loading') {
                document.addEventListener('DOMContentLoaded', initCourseSystem);
            } else {
                initCourseSystem();
            }

            function initCourseSystem() {
                console.log('üöÄ Course system initializing...');

                // Get all necessary elements
                const tabs = document.querySelectorAll('.tab');
                const courseCards = document.querySelectorAll('.course-card');
                const navItems = document.querySelectorAll('.course-nav-item');
                const searchInput = document.getElementById('courseSearch');
                const courseGrid = document.getElementById('courseGrid');

                // State management
                let currentCategory = 'all';
                let currentMode = 'all'; // Start with 'all' to show everything
                let searchTerm = '';

                console.log('‚úÖ Found elements:', {
                    tabs: tabs.length,
                    cards: courseCards.length,
                    navItems: navItems.length,
                    searchInput: searchInput ? 'Found' : 'Missing',
                    courseGrid: courseGrid ? 'Found' : 'Missing'
                });

                if (courseCards.length === 0) {
                    console.error('‚ùå No course cards found! Check your HTML structure.');
                    return;
                }

                // Tab functionality (All/Online/Offline filter)
                tabs.forEach((tab) => {
                    tab.addEventListener('click', function (e) {
                        e.preventDefault();
                        e.stopPropagation();

                        const filter = this.getAttribute('data-filter');
                        console.log(`üîµ Tab clicked: ${filter}`);

                        // Update current mode
                        currentMode = filter;

                        // Update active tab
                        tabs.forEach(t => t.classList.remove('active'));
                        this.classList.add('active');

                        console.log(`Current mode: ${currentMode}`);

                        // Filter courses
                        filterCourses();
                    });
                });

                // Category navigation functionality
                navItems.forEach((item) => {
                    item.addEventListener('click', function (e) {
                        e.preventDefault();
                        e.stopPropagation();

                        const category = this.getAttribute('data-category');
                        console.log(`üìÅ Category clicked: ${category}`);

                        // Update active state
                        navItems.forEach(i => i.classList.remove('active'));
                        this.classList.add('active');

                        // Update current category
                        currentCategory = category;
                        console.log(`Current category: ${currentCategory}`);

                        // Filter courses
                        filterCourses();
                    });
                });

                // Search functionality with debouncing
                let searchTimeout;
                if (searchInput) {
                    searchInput.addEventListener('input', function (e) {
                        clearTimeout(searchTimeout);
                        searchTimeout = setTimeout(() => {
                            searchTerm = e.target.value.toLowerCase().trim();
                            console.log(`üîç Search: "${searchTerm}"`);
                            filterCourses();
                        }, 300); // 300ms debounce
                    });
                }

                // Main filter function
                function filterCourses() {
                    let visibleCount = 0;

                    console.log('üîÑ Filtering courses...', {
                        searchTerm: searchTerm || '(none)',
                        category: currentCategory,
                        mode: currentMode
                    });

                    courseCards.forEach((card) => {
                        const title = (card.getAttribute('data-title') || '').toLowerCase();
                        const description = (card.getAttribute('data-description') || '').toLowerCase();
                        const category = card.getAttribute('data-category') || '';
                        const mode = card.getAttribute('data-mode') || 'both';

                        // Check search match
                        const matchesSearch = !searchTerm ||
                            title.includes(searchTerm) ||
                            description.includes(searchTerm) ||
                            category.includes(searchTerm);

                        // Check category match
                        const matchesCategory = currentCategory === 'all' || category === currentCategory;

                        // Check mode match - NEW LOGIC
                        let matchesMode = false;
                        if (currentMode === 'all') {
                            matchesMode = true; // Show all courses when "All" is selected
                        } else if (mode === 'both') {
                            matchesMode = true; // Courses with "both" show in online and offline
                        } else {
                            matchesMode = mode === currentMode; // Must match exactly
                        }

                        // Debug individual card
                        const isVisible = matchesSearch && matchesCategory && matchesMode;

                        console.log(`${title.substring(0, 25).padEnd(25)} | Search:${matchesSearch ? '‚úì' : '‚úó'} Cat:${matchesCategory ? '‚úì' : '‚úó'} Mode:${matchesMode ? '‚úì' : '‚úó'} = ${isVisible ? 'SHOW' : 'HIDE'}`);

                        // Show or hide card
                        if (isVisible) {
                            card.classList.remove('hidden');
                            card.style.display = '';
                            visibleCount++;
                        } else {
                            card.classList.add('hidden');
                            card.style.display = 'none';
                        }
                    });

                    // Handle no results message
                    updateNoResultsMessage(visibleCount);

                    console.log(`‚úÖ Filtering complete: ${visibleCount}/${courseCards.length} courses visible`);
                }

                // Update no results message
                function updateNoResultsMessage(visibleCount) {
                    const existingNoResults = document.querySelector('.no-results');
                    if (existingNoResults) {
                        existingNoResults.remove();
                    }

                    if (visibleCount === 0 && courseGrid) {
                        const noResults = document.createElement('div');
                        noResults.className = 'no-results';
                        noResults.style.cssText = `
                            grid-column: 1 / -1;
                            text-align: center;
                            padding: 60px 20px;
                            color: #666;
                        `;
                        noResults.innerHTML = `
                            <svg width="64" height="64" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" style="margin: 0 auto 16px; display: block; opacity: 0.5;">
                                <circle cx="11" cy="11" r="8"></circle>
                                <path d="m21 21-4.35-4.35"></path>
                                <line x1="11" y1="8" x2="11" y2="14"></line>
                                <line x1="11" y1="16" x2="11.01" y2="16"></line>
                            </svg>
                            <p style="font-size: 18px; font-weight: 600; margin-bottom: 8px; color: #333;">No courses found</p>
                            <p style="font-size: 14px; color: #666;">Try adjusting your search or filter criteria</p>
                        `;
                        courseGrid.appendChild(noResults);
                    }
                }

                // Initialize - show all courses
                filterCourses();

                console.log('üéâ Course system initialized successfully!');
            }

            // Add CSS for animations and transitions
            const style = document.createElement('style');
            style.textContent = `
                @keyframes fadeIn {
                    from {
                        opacity: 0;
                        transform: translateY(10px);
                    }
                    to {
                        opacity: 1;
                        transform: translateY(0);
                    }
                }
                
                .course-card {
                    animation: fadeIn 0.3s ease-in;
                    transition: opacity 0.3s ease, transform 0.3s ease;
                }
                
                .course-card.hidden {
                    display: none !important;
                }
                
                .tab {
                    cursor: pointer;
                    transition: all 0.3s ease;
                }
                
                .tab.active {
                    background: #0A4D8C;
                    color: white;
                    font-weight: 600;
                }
            `;
            document.head.appendChild(style);
        })();
